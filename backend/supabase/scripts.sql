create table public.tai_san_phong (
  ma_tai_san bigint generated by default as identity primary key,
  ma_phong bigint not null references public.phong(ma_phong) on delete cascade,
  ma_tai_san_hien_thi varchar(50) not null,
  ten_tai_san varchar(255) not null,
  danh_muc varchar(100) not null,
  muc_boi_thuong_mac_dinh numeric(14,2) not null default 0 check (muc_boi_thuong_mac_dinh >= 0),
  created_at timestamptz not null default now(),
