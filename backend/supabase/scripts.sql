create table public.ho_so (
  ma_ho_so bigint generated by default as identity primary key,
  ma_nguoi_dung_xac_thuc uuid unique references auth.users(id) on delete set null,
  vai_tro varchar(50) not null default 'KHACH_HANG',
  ho_ten varchar(150),
  email varchar(255) unique,
  so_dien_thoai varchar(20),
  dia_chi_thuong_tru text,
  avatar_url text,
  ngan_hang_ten varchar(100),
  ngan_hang_so_tai_khoan varchar(50),
  ngan_hang_chu_tai_khoan varchar(150),
  so_cccd varchar(20),
  ngay_cap_cccd date,
