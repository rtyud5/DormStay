  cccd_mat_sau_url text,
  lien_he_khan_cap_ho_ten varchar(150),
  lien_he_khan_cap_sdt varchar(20),
  lien_he_khan_cap_moi_quan_he varchar(50),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.toa (
  ma_toa bigint generated by default as identity primary key,
  ma_dinh_danh varchar(100) not null unique,
  ten varchar(255) not null,
  dia_chi text,
  quan_huyen varchar(100),
  thanh_pho varchar(100),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.tang (
  ma_tang bigint generated by default as identity primary key,
  ma_toa bigint not null references public.toa(ma_toa) on delete cascade,
  so_tang integer not null,
  ten_tang varchar(100),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint uq_tang_toa_so_tang unique (ma_toa, so_tang)
);

create type public.gioi_tinh_type as enum ('Nam', 'Nữ', 'Nam/Nữ');
create table public.phong (
  ma_phong bigint generated by default as identity primary key,
  ma_toa bigint not null references public.toa(ma_toa) on delete restrict,
  ma_tang bigint not null references public.tang(ma_tang) on delete restrict,
  ma_phong_hien_thi varchar(50) not null,
  loai_phong varchar(50) not null,
  suc_chua integer not null default 0 check (suc_chua >= 0),
  gia_thang numeric(14,2) not null default 0 check (gia_thang >= 0),
  trang_thai varchar(50) not null default 'TRONG',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint uq_phong_toa_ma_hien_thi unique (ma_toa, ma_phong_hien_thi)
);

alter table public.phong 
add column if not exists gioi_tinh public.gioi_tinh_type default 'Nam/Nữ';

create table public.giuong (
  ma_giuong bigint generated by default as identity primary key,
  ma_phong bigint not null references public.phong(ma_phong) on delete cascade,
  ma_giuong_hien_thi varchar(50) not null,
  nhan_giuong varchar(50),
  gia_thang numeric(14,2) not null default 0 check (gia_thang >= 0),
  trang_thai varchar(50) not null default 'TRONG',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint uq_giuong_phong_ma_hien_thi unique (ma_phong, ma_giuong_hien_thi)
);

create table public.hinh_anh_phong (
  ma_hinh_anh bigint generated by default as identity primary key,
  ma_phong bigint not null references public.phong(ma_phong) on delete cascade,
  duong_dan_cong_khai text not null,
  la_anh_bia boolean not null default false,
  thu_tu_hien_thi integer not null default 0 check (thu_tu_hien_thi >= 0),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
